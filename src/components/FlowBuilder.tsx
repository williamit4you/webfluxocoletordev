  function toggleStepUser(userId: string) {
    const currentUserIds = currentStep?.assignedUserIds ?? [];
    updateStep(editingStep, {
      assignedUserIds: currentUserIds.includes(userId)
        ? currentUserIds.filter(id => id !== userId)
        : [...currentUserIds, userId]
    });
  }

  async function copyReference(reference: string) {
    try {
      await navigator.clipboard.writeText(reference);
      setCopiedReference(reference);
      window.setTimeout(() => setCopiedReference(current => current === reference ? "" : current), 1800);
    } catch {
      setCopiedReference("");
    }
  }

  function insertIntoApiConfig(fieldName: "url" | "queryTemplate", reference: string) {
    const currentValue = currentStep?.apiConfig?.[fieldName] ?? "";
    updateApiConfig(editingStep, { [fieldName]: `${currentValue}${reference}` });
  }

  function toggleSendFieldKey(fieldKey: string) {
    const currentKeys = currentStep?.apiConfig?.sendFieldKeys ?? [];
    const nextKeys = currentKeys.includes(fieldKey)
      ? currentKeys.filter(key => key !== fieldKey)
      : [...currentKeys, fieldKey];

    updateApiConfig(editingStep, { sendFieldKeys: nextKeys });
  }

  function selectAllSendFields() {
    updateApiConfig(editingStep, { sendFieldKeys: availableIntegrationFields.map(field => field.key) });
  }

  function clearSendFields() {
    updateApiConfig(editingStep, { sendFieldKeys: [] });
  }

  function moveSendField(fieldKey: string, direction: "up" | "down") {
    const currentKeys = currentStep?.apiConfig?.sendFieldKeys ?? [];
    if (currentKeys.length < 2) {
      return;
    }

    const index = currentKeys.indexOf(fieldKey);
    if (index < 0) {
      return;
    }

    const nextIndex = direction === "up" ? index - 1 : index + 1;
    if (nextIndex < 0 || nextIndex >= currentKeys.length) {
      return;
    }

    const reordered = [...currentKeys];
    [reordered[index], reordered[nextIndex]] = [reordered[nextIndex], reordered[index]];
    updateApiConfig(editingStep, { sendFieldKeys: reordered });
  }

