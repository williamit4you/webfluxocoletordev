function slugify(value: string) {
  return value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^\w]+/g, "_")
    .replace(/^_+|_+$/g, "")
    .toLowerCase();
}

function buildAutoFieldKey(previousLabel: string, currentKey: string, nextLabel: string) {
  const previousAutoKey = slugify(previousLabel);
  const nextAutoKey = slugify(nextLabel);
  const isAutoGenerated = !currentKey || currentKey === previousAutoKey;

  return isAutoGenerated ? nextAutoKey : currentKey;
}

