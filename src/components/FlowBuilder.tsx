  function handleStepDrop(index: number) {
    if (!isDraft || dragStepIndex === null) {
      return;
    }

    reorderStep(dragStepIndex, index);
    setDragStepIndex(null);
    setDropStepIndex(null);
  }

  function handleStepDragEnd() {
    setDragStepIndex(null);
    setDropStepIndex(null);
  }

