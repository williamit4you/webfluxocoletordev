  async function saveFlow() {
    if (flowId && !isDraft) {
      setError("Crie um rascunho antes de alterar uma versao publicada.");
      return;
    }

    setSaving(true);
    setError("");
    setSuccess("");

    const payload = {
      name,
      description,
      active,
      assignedUserIds,
      tokens: tokens.map(token => ({
        ...token,
        name: token.name.trim(),
        headerName: token.headerName?.trim(),
        value: token.value ?? ""
      })),
      steps: steps.map((step, stepIndex) => ({
        ...step,
        order: stepIndex + 1,
        fields: step.fields.map((field, fieldIndex) => ({
          ...field,
          order: fieldIndex + 1,
          key: field.key.trim(),
          label: field.label.trim(),
          options: field.options.map((option, optionIndex) => ({
            ...option,
            order: optionIndex + 1,
            label: option.label.trim(),
            value: option.value.trim(),
            key: field.type === 5 ? (option.key?.trim() || option.value.trim() || null) : (option.key?.trim() || null),
            type: field.type === 5 ? (option.type ?? 0) : (option.type ?? null),
            mask: option.mask?.trim() || null,
            required: option.required ?? false
          }))
        }))
      }))
    };

    try {
      if (flowId) {
        const result = await api<{ id: string }>(`/flows/${flowId}`, { method: "PUT", body: JSON.stringify(payload) });
        if (result.id && result.id !== flowId) {
          router.replace(`/fluxos/${result.id}`);
          return;
        }
        await reloadFlowState("Rascunho salvo com sucesso.");
      } else {
        const result = await api<{ id: string }>(`/flows`, { method: "POST", body: JSON.stringify(payload) });
        router.replace(`/fluxos/${result.id}`);
        return;
      }
    } catch (e) {
      setError(e instanceof Error ? e.message : "Nao foi possivel salvar o fluxo.");
    } finally {
      setSaving(false);
    }
  }

